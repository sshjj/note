class Solution {
public:
    Node* used[101];
    Node* cloneGraph(Node* node) {
        if(!node)return node;
        if(used[node->val])return used[node->val];
        Node* p = new Node(node->val);
        used[node->val] = p;
        vector<Node*> tp = node->neighbors;
        for(int i = 0;i<tp.size();i++){
            p->neighbors.push_back(cloneGraph(tp[i]));
        }
        return p;
    }
};